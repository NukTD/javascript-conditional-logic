// Switch Statement
let lightBulbStatus = "On";
lightBulbStatus = "Broken";

//Start coding here
switch (lightBulbStatus) {
    case "On":
    console.log("Light bulb is On.")
    break;
    case "Off":
    console.log("Light bulb is Off")
    break;
    case "Broken":
    console.log("Light bulb is Broken")
    break;
}

